/**
 * Space.HSV
 * =========
 *
 * The `com.lhkbob.image.color.space.hsv` package defines color spaces and the necessary
 * transformations for representing colors stored as hue, saturation, and value ({@link
 * com.lhkbob.imaje.color.HSV HSV}) or hue, lightness, saturation ({@link com.lhkbob.imaje.color.HLS
 * HLS}). The former is represented by {@link com.lhkbob.imaje.color.space.hsv.HSVSpace} and the
 * latter by {@link com.lhkbob.imaje.color.space.hsv.HLSSpace}.
 *
 * Both of these are secondary color spaces that must be defined relative to an RGB color space,
 * most likely the {@link com.lhkbob.imaje.color.space.rgb.SRGB} space.
 *
 * @author Michael Ludwig
 */
